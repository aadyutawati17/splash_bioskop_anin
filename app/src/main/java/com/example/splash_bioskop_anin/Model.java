package com.example.splash_bioskop_anin;

public class Model {
    private String nama;
    private String teather;
    private String nama_film;
    private int jam;

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getTeather() {
        return teather;
    }

    public void setTeather(String teather) {
        this.teather = teather;
    }

    public String getNama_film() {
        return nama_film;
    }

    public void setNama_film(String nama_film) {
        this.nama_film = nama_film;
    }

    public int getJam() {
        return jam;
    }

    public void setJam(int jam) {
        this.jam = jam;
    }
}
